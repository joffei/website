<h2><b>Still I Stand</b></h2>
<p>You may beat me to the ground<br>
Give me the back of your hand<br>
Kick me when I'm down<br>
But strong as ever, I stand<br>
<br>
Why do you beat me?<br>
Have I done something wrong?<br>
That my happiness gets to you<br>
Like a Bruno mars song<br>
<br>
You may have been best in class<br>
Fairest in all the land<br>
But with a smile on my face<br>
I will stand<br>
<br>
When your voice smacks me in the face<br>
And I don't fight back<br>
I'm building my strength<br>
For the ultimate attack<br>
<br>
you've broken my legs<br>
put me on a movement ban<br>
but I'll heal and I'll learn<br>
to run, to walk, to stand</p>